document.addEventListener("click", (event) => {
  const favoriteButton = event.target.closest(".favorite-button[data-action='favorite']");
  if (favoriteButton) {
    event.preventDefault();
    event.stopPropagation();
    toggleFavorite(favoriteButton.dataset.id);
    return;
  }

  const button = event.target.closest("[data-action]");
  if (!button) return;

  const action = button.dataset.action;

  if (action === "lang") {
    updateLanguage(button.dataset.lang);
    return;
  }

  if (action === "goto-login") {
    navigateTo("login");
    return;
  }

  if (action === "auth-mode-login") {
    state.loginForm.mode = "login";
    state.loginForm.step = "request";
    state.authMessage = "";
    render();
    return;
  }

  if (action === "auth-mode-signup") {
    state.loginForm.mode = "signup";
    state.loginForm.step = "request";
    state.authMessage = "";
    render();
    return;
  }

  if (action === "auth-mode-reset") {
    state.loginForm.mode = "reset";
    state.authMessage = "";
    render();
    return;
  }

  if (action === "goto-profile") {
    navigateTo("profile");
    return;
  }

  if (action === "goto-generator") {
    navigateTo("generator");
    return;
  }

  if (action === "submit-login") {
    login();
    return;
  }

  if (action === "verify-otp") {
    verifyOtp();
    return;
  }

  if (action === "request-password-reset") {
    requestPasswordReset();
    return;
  }

  if (action === "verify-password-reset") {
    verifyPasswordReset();
    return;
  }

  if (action === "change-password") {
    changePassword();
    return;
  }

  if (action === "google-login") {
    if (state.authConfig.googleEnabled) {
      window.location.href = "/api/auth/google/start";
      return;
    }
    state.authMessage = getTexts().googleComingSoon;
    render();
    return;
  }

  if (action === "logout") {
    logout();
    return;
  }

  if (action === "sample") {
    setSampleData();
    return;
  }

  if (action === "generate") {
    generateFromBackend(false);
    return;
  }

  if (action === "improve") {
    generateFromBackend(true);
    return;
  }

  if (action === "copy") {
    copyContent();
    return;
  }

  if (action === "download-doc") {
    downloadDoc();
    return;
  }

  if (action === "add-attr") {
    state.form.attributes.push(createDefaultAttribute());
    render();
    return;
  }

  if (action === "remove-attr") {
    state.form.attributes = state.form.attributes.filter((item) => item.id !== button.dataset.id);
    ensureFormDefaults();
    render();
    return;
  }

  if (action === "remove-image") {
    removeImage(button.dataset.id);
    return;
  }

  if (action === "open-version") {
    const matched = state.history.find((item) => item.id === button.dataset.id);
    state.activeVersionId = button.dataset.id;
    if (matched) restoreFormFromVersion(matched);
    render();
    return;
  }

  if (action === "delete-history") {
    deleteHistoryItem(button.dataset.id);
    return;
  }

  if (action === "open-favorite") {
    const matched = state.history.find((item) => item.id === button.dataset.id);
    if (matched) {
      state.activeVersionId = matched.id;
      restoreFormFromVersion(matched);
      navigateTo("generator");
    }
    return;
  }

  if (action === "trigger-upload") {
    const input = document.querySelector('[data-role="file-input"]');
    if (input) input.click();
    return;
  }
});

document.addEventListener("input", (event) => {
  const target = event.target;

  if (target.matches("[data-model]")) {
    const key = target.dataset.model;
    if (key === "category") {
      applyCategoryChange(target.value);
      render();
      return;
    }

    state.form[key] = target.type === "select-one" || target.tagName === "SELECT" ? Number(target.value) : target.value;
    return;
  }

  if (target.matches("[data-login-field]")) {
    state.loginForm[target.dataset.loginField] = target.value;
    return;
  }

  if (target.matches("[data-reset-field]")) {
    state.resetForm[target.dataset.resetField] = target.value;
    return;
  }

  if (target.matches("[data-profile-password]")) {
    state.profilePasswordForm[target.dataset.profilePassword] = target.value;
    return;
  }

  if (target.matches("[data-highlight-index]")) {
    state.form.highlights[Number(target.dataset.highlightIndex)] = target.value;
    return;
  }

  if (target.matches('[data-model="highlightsText"]')) {
    state.form.highlights = target.value
      .split("\n")
      .map((item) => item.trim())
      .filter(Boolean)
      .slice(0, 6);
    return;
  }

  if (target.matches('[data-role="image-block-select"]')) {
    const active = getActiveVersion();
    if (!active) return;
    const block = active.blocks.find((item) => item.id === target.dataset.blockId);
    if (!block) return;
    block.imageId = target.value;
    return;
  }

  if (target.matches("[data-attr-id][data-field]")) {
    const item = state.form.attributes.find((attr) => attr.id === target.dataset.attrId);
    if (!item) return;
    if (target.dataset.field === "type") {
      item.type = Number(target.value);
    } else {
      item.value = target.value;
    }
    return;
  }
});

document.addEventListener("change", (event) => {
  const target = event.target;
  if (target.matches('[data-role="file-input"]')) {
    onFilesSelected(target.files);
    target.value = "";
  }
});

document.addEventListener("focusout", (event) => {
  const block = event.target.closest('[data-role="text-block"]');
  if (!block) return;
  const active = getActiveVersion();
  if (!active) return;
  const current = active.blocks.find((item) => item.id === block.dataset.blockId);
  if (!current) return;
  current.content = block.innerText.trim();
});

