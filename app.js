function syncVersionsWithCurrentImages() {
  if (!state.history.length) return;

  const slots = buildImageSlots();
  state.history = state.history.map((version) => {
    const textBlocks = version.blocks.filter((block) => block.type === "text");
    const imageBlocks = slots.map((slot, index) => {
      const previousImageBlock = version.blocks.filter((block) => block.type === "image")[index];
      return {
        ...slot,
        id: previousImageBlock?.id || slot.id
      };
    });

    const blocks = [];
    if (imageBlocks[0]) blocks.push(imageBlocks[0]);
    if (textBlocks[0]) blocks.push(textBlocks[0]);
    if (imageBlocks[1]) blocks.push(imageBlocks[1]);
    if (textBlocks[1]) blocks.push(textBlocks[1]);
    if (imageBlocks[2]) blocks.push(imageBlocks[2]);
    if (textBlocks[2]) blocks.push(textBlocks[2]);

    for (let index = 3; index < textBlocks.length; index += 1) {
      blocks.push(textBlocks[index]);
    }

    return { ...version, blocks };
  });
}

