async function buildRichHtmlPayload(active) {
  const embeddedImageMap = new Map();
  await Promise.all(
    active.blocks.map(async (block) => {
      if (block.type !== "image") return;
      const image = getImageById(block.imageId);
      if (!image) return;
      try {
        const dataUrl = await imageSrcToDataUrl(image.src);
        embeddedImageMap.set(block.id, dataUrl);
      } catch (_error) {
        embeddedImageMap.set(block.id, image.src);
      }
    })
  );

  return active.blocks
    .map((block) => {
      if (block.type === "text") {
        return `<p>${escapeHtml(block.content).replaceAll("<br />", "<br>")}</p>`;
      }

      const image = getImageById(block.imageId);
      if (!image) {
        return `<p>[${block.label}]</p>`;
      }

      const embeddedSrc = embeddedImageMap.get(block.id) || image.src;

      return `
        <figure style="margin: 0 0 20px;">
          <img src="${embeddedSrc}" alt="${block.label}" style="display:block;max-width:100%;border-radius:16px;" />
          <figcaption style="margin-top:8px;font-family:JetBrains Mono,monospace;font-size:12px;color:#6d645b;">${block.label}</figcaption>
        </figure>
      `;
    })
    .join("");
}

function wrapCanvasText(ctx, text, maxWidth) {
  const paragraphs = text.split("\n");
  const lines = [];

  paragraphs.forEach((paragraph, paragraphIndex) => {
    if (!paragraph.trim()) {
      lines.push("");
      return;
    }

    const words = paragraph.split(/\s+/);
    let currentLine = words[0] || "";

    for (let index = 1; index < words.length; index += 1) {
      const nextLine = `${currentLine} ${words[index]}`;
      if (ctx.measureText(nextLine).width <= maxWidth) {
        currentLine = nextLine;
      } else {
        lines.push(currentLine);
        currentLine = words[index];
      }
    }

    lines.push(currentLine);

    if (paragraphIndex < paragraphs.length - 1) {
      lines.push("");
    }
  });

  return lines;
}

async function buildCompositeClipboardImage(active) {
  const width = 1080;
  const padding = 40;
  const contentWidth = width - padding * 2;
  const textFont = "28px Manrope, sans-serif";
  const labelFont = "600 20px JetBrains Mono, monospace";
  const textLineHeight = 44;
  const blockGap = 28;
  const imageHeight = 360;

  const measureCanvas = document.createElement("canvas");
  const measureCtx = measureCanvas.getContext("2d");
  measureCtx.font = textFont;

  const preparedBlocks = await Promise.all(
    active.blocks.map(async (block) => {
      if (block.type === "text") {
        const lines = wrapCanvasText(measureCtx, block.content, contentWidth - 36);
        const height = Math.max(140, lines.length * textLineHeight + 36);
        return { ...block, lines, height };
      }

      const image = getImageById(block.imageId);
      const loadedImage = image ? await loadImageElement(image.src) : null;
      return { ...block, loadedImage, height: imageHeight };
    })
  );

  const totalHeight = preparedBlocks.reduce((sum, block) => sum + block.height, 0) + blockGap * (preparedBlocks.length - 1) + padding * 2;
  const canvas = document.createElement("canvas");
  canvas.width = width;
  canvas.height = totalHeight;
  const ctx = canvas.getContext("2d");

  ctx.fillStyle = "#fffaf5";
  ctx.fillRect(0, 0, width, totalHeight);

  let cursorY = padding;

  for (const block of preparedBlocks) {
    if (block.type === "text") {
      roundedRectPath(ctx, padding, cursorY, contentWidth, block.height, 24);
      ctx.fillStyle = "#ffffff";
      ctx.fill();
      ctx.strokeStyle = "rgba(48, 34, 20, 0.08)";
      ctx.lineWidth = 2;
      ctx.stroke();

      ctx.fillStyle = "#171411";
      ctx.font = textFont;
      let lineY = cursorY + 54;
      block.lines.forEach((line) => {
        if (line === "") {
          lineY += textLineHeight * 0.6;
          return;
        }
        ctx.fillText(line, padding + 18, lineY);
        lineY += textLineHeight;
      });
    } else {
      roundedRectPath(ctx, padding, cursorY, contentWidth, block.height, 24);
      ctx.save();
      ctx.clip();
      if (block.loadedImage) {
        const ratio = Math.max(contentWidth / block.loadedImage.width, block.height / block.loadedImage.height);
        const drawWidth = block.loadedImage.width * ratio;
        const drawHeight = block.loadedImage.height * ratio;
        const drawX = padding + (contentWidth - drawWidth) / 2;
        const drawY = cursorY + (block.height - drawHeight) / 2;
        ctx.drawImage(block.loadedImage, drawX, drawY, drawWidth, drawHeight);
      } else {
        ctx.fillStyle = "#f3ede4";
        ctx.fillRect(padding, cursorY, contentWidth, block.height);
      }
      ctx.restore();

      ctx.strokeStyle = "rgba(48, 34, 20, 0.08)";
      ctx.lineWidth = 2;
      roundedRectPath(ctx, padding, cursorY, contentWidth, block.height, 24);
      ctx.stroke();

      const tagWidth = Math.max(180, ctx.measureText(block.label).width + 30);
      roundedRectPath(ctx, padding + 18, cursorY + block.height - 58, tagWidth, 38, 19);
      ctx.fillStyle = "rgba(255,255,255,0.86)";
      ctx.fill();
      ctx.font = labelFont;
      ctx.fillStyle = "#6d645b";
      ctx.fillText(block.label, padding + 34, cursorY + block.height - 31);
    }

    cursorY += block.height + blockGap;
  }

  return new Promise((resolve, reject) => {
    canvas.toBlob((blob) => {
      if (blob) resolve(blob);
      else reject(new Error("Unable to create image blob"));
    }, "image/png");
  });
}

async function copyContent() {
  const active = getActiveVersion();
  if (!active) return;

  let imageBlob = null;
  try {
    imageBlob = await buildCompositeClipboardImage(active);
  } catch (_error) {
    imageBlob = null;
  }

  const textPayload = active.blocks
    .map((block) => (block.type === "text" ? block.content : `[${block.label}]`))
    .join("\n\n") + (active.hashtags?.length ? `\n\n${getTexts().hashtags}: ${active.hashtags.join(" ")}` : "");

  const htmlPayload = await buildRichHtmlPayload(active);
  const hashtagsHtml = active.hashtags?.length
    ? `<p>${active.hashtags.join(" ")}</p>`
    : "";

  const clipboardItem = typeof ClipboardItem !== "undefined"
    ? new ClipboardItem({
        "text/plain": new Blob([textPayload], { type: "text/plain" }),
        "text/html": new Blob([htmlPayload], { type: "text/html" }),
        ...(imageBlob ? { "image/png": imageBlob } : {})
      })
    : null;

  const onSuccess = () => {
    const notice = document.querySelector("[data-copy-status]");
    if (!notice) return;
    notice.textContent = getTexts().copyDone;
    window.setTimeout(() => {
      notice.textContent = "";
    }, 1800);
  };

  if (clipboardItem && navigator.clipboard?.write) {
    navigator.clipboard.write([clipboardItem]).then(onSuccess).catch(() => {
      navigator.clipboard.writeText(textPayload).then(onSuccess);
    });
    return;
  }

  navigator.clipboard.writeText(textPayload).then(onSuccess);
}

